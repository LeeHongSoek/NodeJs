        methods: {
            change: function (event) {
                const myRibbon = this.$refs.myRibbon;
                const checked = event.args.checked;
                const mode = checked ? 'popup' : 'default';
                const width = checked ? 115 : 425;

                myRibbon.setOptions({ width: width, mode: mode });

                if (mode == 'popup') {
                    myRibbon.setPopupLayout(0, 'default', 310, '100%');
